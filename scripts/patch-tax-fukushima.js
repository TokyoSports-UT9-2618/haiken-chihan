const muniPath = path.join(__dirname, '..', 'data', '07', 'municipalities.json');
const munis    = JSON.parse(fs.readFileSync(muniPath, 'utf8'));

let patched = 0;
for (const m of munis) {
  const tax = MUNI_TAX[m.name];
  if (tax !== undefined) {
    m.taxRevenue = tax * 1000; // 千円 → 円
    patched++;
  }
}

fs.writeFileSync(muniPath, JSON.stringify(munis, null, 2), 'utf8');
console.log(`税収データを補完: ${patched}/${munis.length} 件`);
